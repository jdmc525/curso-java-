    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        
        String nota = JOptionPane.showInputDialog(this, "Digite la nota a agregar" );
        
        try{
             double notaN = Double.parseDouble(nota.trim());
         
            if (nota != null) {
                  if (  nota.trim().length() >0 ) {
                        modeloLista.addElement((nota.trim() + "%"));
                    }
            }
            
            promedio.setText( promedio());
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, "La entrada debe ser de tipo numero");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        
        if (listaNotas.getSelectedIndex() >= 0) {
            String valor =  listaNotas.getSelectedValue().trim().substring(0 , (listaNotas.getSelectedValue().trim().length()-1) );
            
            String nValor  = JOptionPane.showInputDialog(this, "Digite el nuevo valor" , valor );
            
            try{
                double a = Double.parseDouble(nValor.trim());
            
                if (nValor != null) {
                    if (nValor.trim().length() >= 0) {
                        modeloLista.setElementAt(nValor + "%", listaNotas.getSelectedIndex());
                    }
                }

                 promedio.setText( promedio());

            }catch(Exception ex){
                 JOptionPane.showMessageDialog(this, "Error, el valor debe ser un numero" );
            }
        }
    }//GEN-LAST:event_btnEditarActionPerformed

