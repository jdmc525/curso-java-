   //Funcion para calcular promedio de notas ingresadas
   private String promedio(){
       double suma = 0, promedio;
       DecimalFormat f = new DecimalFormat();
       f.setMaximumFractionDigits(4);
       f.setMinimumFractionDigits(1);
       
        for (int i = 0; i < modeloLista.size(); i++) {
            String elementos = modeloLista.getElementAt(i).toString().trim();

            suma +=  Double.parseDouble(elementos.substring(0, (modeloLista.getElementAt(i).toString().trim().length()-1)));
        }

        promedio = suma/ modeloLista.size();

       return (f.format(promedio) + "%");
   } 
   
   
   
