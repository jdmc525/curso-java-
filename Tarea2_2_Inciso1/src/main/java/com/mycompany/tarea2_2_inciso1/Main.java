    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
          try{
           double mayor = Double.MIN_VALUE;
            double valores[] = { Double.parseDouble(num1.getText().trim()) , Double.parseDouble(num2.getText().trim()),
           Double.parseDouble(num3.getText().trim()) , Double.parseDouble(num4.getText().trim())};
           
           
           for (int i = 0; i < valores.length ; i++) {
               if (  valores[i] > mayor) {
                   mayor = valores[i];
               }
           }
           
           resultado.setText(String.valueOf(mayor) );
           
           
          }catch(Exception ex){
              JOptionPane.showMessageDialog(this, "Error, los datos deben ser n√∫meros");
          }
    }//GEN-LAST:event_btnCalcularActionPerformed

