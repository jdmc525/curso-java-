    private void rojoStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_rojoStateChanged
        //Cambiar el fondo de la pantalla al seleccionar una opcion
        
        if (rojo.isSelected() == true) {
            this.getContentPane().setBackground(Color.red);
        }
        if (amarillo.isSelected() == true) {
            this.getContentPane().setBackground(Color.yellow);
        }
        if (verde.isSelected() == true) {
            this.getContentPane().setBackground(Color.green);
        }
    }//GEN-LAST:event_rojoStateChanged

