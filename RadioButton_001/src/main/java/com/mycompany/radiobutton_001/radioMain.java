    private void btnProbarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProbarActionPerformed
        //Obligar a seleccionar alguno de los generos
        
        if (masculino.isSelected() == false && femenino.isSelected() == false) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un genero");
            return; //no continuar 
        }
        if (masculino.isSelected() == true) {
            JOptionPane.showMessageDialog(this, "El genero es Masculino");
            return; //no continuar 
        }
        if (femenino.isSelected() == true) {
            JOptionPane.showMessageDialog(this, "El genero es Femenino");
            return; //no continuar 
        }
      
    }//GEN-LAST:event_btnProbarActionPerformed

