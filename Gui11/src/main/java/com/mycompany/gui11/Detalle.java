    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        //Validar que los datos esten correctos
        
        try{
            if (codigo.getText().trim().length() == 0){
                JOptionPane.showMessageDialog(this, "Debe llenar el codigo");
                return; //Terminar el metodo
            }
            
            if (nombre.getText().trim().length() == 0){
                JOptionPane.showMessageDialog(this, "Debe llenar el nombre de la mascota");
                return; //Terminar el metodo
            }
            
            if (especie.getText().trim().length() == 0){
                JOptionPane.showMessageDialog(this, "Debe llenar la especie");
                return; //Terminar el metodo
            }
            
            
            if (propietario.getText().trim().length() == 0){
                JOptionPane.showMessageDialog(this, "Debe llenar el nombre del propietario");
                return; //Terminar el metodo
            }
            
            
            if (Double.parseDouble(edad.getText()) < 0){
                JOptionPane.showMessageDialog(this, "La edad debe ser mayor que cero");
                return; //Terminar el metodo
            }
        
            //Si llego aca entonces todo bien, se superaron las validaciones 
            
            this.setVisible(false);  //Ocultar JDialog para enviar datos a Principal
            
            
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, "Revise que la edad sea un valor numÃ©rico");
        
        }
        
        
        
        
    }//GEN-LAST:event_btnAceptarActionPerformed

